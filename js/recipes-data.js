export function findProduct(products, name, unit) {
  const matches = products.filter((p) => p.name === name);
  if (matches.length <= 1) return matches[0] || null;
  if (unit) {
    const withUnit = matches.find((p) => p.unit === unit);
    if (withUnit) return withUnit;
  }
  return matches[0];
}
